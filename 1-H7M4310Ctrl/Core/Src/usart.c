/*vofa发送api*/
void sent_vofa(float f1,float f2,float f3,float f4)
{
	union Trm {
	uint8_t data[4];
	float dataf;
	};

	uint8_t vofa[8] = {0};
	uint8_t tail[4] = {0x00, 0x00, 0x80, 0x7f};
	
	union Trm A = {
		.dataf = f1,
	};		
	union Trm B = {
		.dataf = f2,
	};		
	union Trm C = {
		.dataf = f3,
	};		
	union Trm D = {
		.dataf = f4,
	};		
	
	for(uint8_t i = 0;i < 4;i ++)
	{
		vofa[i] = A.data[i];
		vofa[i + 4] = B.data[i];
	}
	HAL_UART_Transmit(&huart1,vofa,8,HAL_MAX_DELAY);
	
	for(uint8_t i = 0;i < 4;i ++)
	{
		vofa[i] = C.data[i];
		vofa[i + 4] = D.data[i];
	}
	HAL_UART_Transmit(&huart1,vofa,8,HAL_MAX_DELAY);
	
	HAL_UART_Transmit(&huart1,tail,4,HAL_MAX_DELAY);
}

